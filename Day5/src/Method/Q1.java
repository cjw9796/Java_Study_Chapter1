package Method;

public class Q1 {
	
	public static int add(int a) {
		
		
			for(a=1;a<=10;a++);
			
			return a;
	}
		
		
	
	

	public static void main(String[] args) {
		// 
		
		int a = 10;
		
		int res = add(a);
		System.out.println("더한 결과:" + res);

	}

}
